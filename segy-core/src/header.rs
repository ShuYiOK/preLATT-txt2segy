impl BinaryHeader {
    pub fn with_revision(mut self, rev: SegyRevision) -> Self {
        self.segy_revision_number = rev.as_u16();
        self
    }

    pub fn write_to<W: Write>(&self, writer: &mut W) -> std::io::Result<()> {
        let mut buf = [0u8; BINARY_HEADER_SIZE];

        let mut cursor = std::io::Cursor::new(&mut buf[..]);

        cursor.write_i32::<BigEndian>(self.job_id)?;
        cursor.write_i32::<BigEndian>(self.line_num)?;
        cursor.write_i32::<BigEndian>(self.reel_num)?;
        cursor.write_u16::<BigEndian>(self.traces_per_ensemble)?;
        cursor.write_u16::<BigEndian>(self.aux_traces_per_ensemble)?;
        cursor.write_u16::<BigEndian>(self.sample_interval)?;
        cursor.write_u16::<BigEndian>(self.sample_interval_original)?;
        cursor.write_u16::<BigEndian>(self.samples_per_trace)?;
        cursor.write_u16::<BigEndian>(self.samples_per_trace_original)?;
        cursor.write_u16::<BigEndian>(self.data_sample_format)?;
        cursor.write_u16::<BigEndian>(self.ensemble_fold)?;
        cursor.write_u16::<BigEndian>(self.trace_sorting_code)?;
        cursor.write_u16::<BigEndian>(self.vertical_sum_code)?;
        cursor.write_u16::<BigEndian>(self.sweep_freq_start)?;
        cursor.write_u16::<BigEndian>(self.sweep_freq_end)?;
        cursor.write_u16::<BigEndian>(self.sweep_length)?;
        cursor.write_u16::<BigEndian>(self.sweep_type_code)?;
        cursor.write_u16::<BigEndian>(self.trace_num_of_sweep_channel)?;
        cursor.write_u16::<BigEndian>(self.sweep_taper_length_start)?;
        cursor.write_u16::<BigEndian>(self.sweep_taper_length_end)?;
        cursor.write_u16::<BigEndian>(self.taper_type)?;
        cursor.write_u16::<BigEndian>(self.correlated_data_traces)?;
        cursor.write_u16::<BigEndian>(self.binary_gain_recovered)?;
        cursor.write_u16::<BigEndian>(self.amplitude_recovery_method)?;
        cursor.write_u16::<BigEndian>(self.measurement_system)?;
        cursor.write_u16::<BigEndian>(self.impulse_signal_polarity)?;
        cursor.write_u16::<BigEndian>(self.vibratory_polarity_code)?;

        cursor.write_all(&[0u8; 240])?;

        cursor.write_u16::<BigEndian>(self.segy_revision_number)?;
        cursor.write_u16::<BigEndian>(self.fixed_length_trace_flag)?;
        cursor.write_u16::<BigEndian>(self.num_extended_text_headers)?;
        cursor.write_u32::<BigEndian>(self.max_additional_trace_headers)?;

        cursor.write_all(&[0u8; 90])?;

        writer.write_all(&buf)?;
        Ok(())
    }
}
