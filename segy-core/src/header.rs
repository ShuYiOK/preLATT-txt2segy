impl TextHeader {
    pub fn set_line(&mut self, line_num: usize, text: &str) {
        if line_num == 0 || line_num > 40 {
            return;
        }
        let padded = format!("C{:02} {}", line_num, text);
        if line_num <= self.lines.len() {
            self.lines[line_num - 1] = padded;
        }
    }

    pub fn write_to<W: Write>(&self, writer: &mut W) -> std::io::Result<()> {
        let mut buf = [0u8; TEXT_HEADER_SIZE];
        for (i, line) in self.lines.iter().take(40).enumerate() {
            let line_bytes = line.as_bytes();
            let copy_len = line_bytes.len().min(80);
            buf[i * 80..i * 80 + copy_len].copy_from_slice(&line_bytes[..copy_len]);
            for j in copy_len..80 {
                buf[i * 80 + j] = b' ';
            }
        }
        writer.write_all(&buf)
    }
}

